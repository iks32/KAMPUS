pi    = 3.14
jari   = float(input("masukan nilai jari jari yang mau dihitung : "))
luas   = pi*(jari*jari)
keliling   = 2*pi*jari
print("-----------------HASIL-----------------")
print("Luas Lingkaran =","{:.3f}".format(luas)) 
print("Keliling Lingkaran =","{:.3f}".format(keliling))
